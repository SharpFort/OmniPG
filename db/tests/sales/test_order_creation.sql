-- pgTAP 测试：订单创建流程
-- 运行方式：pg_prove -U app_owner -d app_db db/tests/sales/test_order_creation.sql
BEGIN;
SELECT plan(8);

-- ============================================================
-- 1. sales schema 存在性检查
-- ============================================================
SELECT has_schema('sales', 'sales schema 存在');

-- ============================================================
-- 2. 订单表存在性检查
-- ============================================================
SELECT has_table('sales_orders', '订单表 sales_orders 存在');

-- ============================================================
-- 3. 订单创建函数存在性
-- ============================================================
SELECT has_function('create_order', ARRAY['jsonb'], 'create_order(jsonb) 函数存在');

-- ============================================================
-- 4. 订单创建成功
-- ============================================================
SELECT lives_ok(
    $$ SELECT create_order('[{"product_id":"00000000-0000-0000-0000-500000000001","quantity":2}]'::jsonb) $$,
    '订单创建成功（有效商品）'
);

-- ============================================================
-- 5. 订单创建失败（无效商品）
-- ============================================================
SELECT throws_ok(
    $$ SELECT create_order('[{"product_id":"00000000-0000-0000-0000-999999999999","quantity":1}]'::jsonb) $$,
    'P0001',
    'Product Not Found',
    '无效商品 ID 抛出 P0001 Product Not Found'
);

-- ============================================================
-- 6. 订单创建失败（库存不足）
-- ============================================================
SELECT throws_ok(
    $$ SELECT create_order('[{"product_id":"00000000-0000-0000-0000-500000000001","quantity":99999}]'::jsonb) $$,
    'P0002',
    'Insufficient Stock',
    '库存不足抛出 P0002 Insufficient Stock'
);

-- ============================================================
-- 7. 订单创建失败（空商品列表）
-- ============================================================
SELECT throws_ok(
    $$ SELECT create_order('[]'::jsonb) $$,
    'P0003',
    'Empty Order',
    '空商品列表抛出 P0003 Empty Order'
);

-- ============================================================
-- 8. 订单创建返回 JSON 格式
-- ============================================================
SELECT lives_ok(
    $$ SELECT jsonb_typeof(create_order('[{"product_id":"00000000-0000-0000-0000-500000000001","quantity":1}]'::jsonb)) $$,
    'create_order 返回 JSON 格式'
);

SELECT * FROM finish();
ROLLBACK;
