-- pgTAP 测试：扣减库存函数
-- 运行方式：pg_prove -U app_owner -d app_db db/tests/inventory/test_stock_deduction.sql
BEGIN;
SELECT plan(8);

-- ============================================================
-- 1. inventory schema 存在性检查
-- ============================================================
SELECT has_schema('inventory', 'inventory schema 存在');

-- ============================================================
-- 2. 库存表存在性检查
-- ============================================================
SELECT has_table('inventory_stock', '库存表 inventory_stock 存在');

-- ============================================================
-- 3. 扣减库存函数存在性
-- ============================================================
SELECT has_function('deduct_stock', ARRAY['uuid', 'integer'], 'deduct_stock(uuid, integer) 函数存在');

-- ============================================================
-- 4. 扣减库存成功
-- ============================================================
SELECT lives_ok(
    $$ SELECT deduct_stock('00000000-0000-0000-0000-500000000001'::uuid, 1) $$,
    '扣减库存成功（有效商品 + 足够库存）'
);

-- ============================================================
-- 5. 扣减库存失败（库存不足）
-- ============================================================
SELECT throws_ok(
    $$ SELECT deduct_stock('00000000-0000-0000-0000-500000000001'::uuid, 99999) $$,
    'P0002',
    'Insufficient Stock',
    '库存不足抛出 P0002 Insufficient Stock'
);

-- ============================================================
-- 6. 扣减库存失败（无效商品）
-- ============================================================
SELECT throws_ok(
    $$ SELECT deduct_stock('00000000-0000-0000-0000-999999999999'::uuid, 1) $$,
    'P0001',
    'Product Not Found',
    '无效商品 ID 抛出 P0001 Product Not Found'
);

-- ============================================================
-- 7. 扣减库存失败（负数数量）
-- ============================================================
SELECT throws_ok(
    $$ SELECT deduct_stock('00000000-0000-0000-0000-500000000001'::uuid, -1) $$,
    'P0004',
    'Invalid Quantity',
    '负数数量抛出 P0004 Invalid Quantity'
);

-- ============================================================
-- 8. 扣减库存返回 BOOLEAN
-- ============================================================
SELECT lives_ok(
    $$ SELECT deduct_stock('00000000-0000-0000-0000-500000000001'::uuid, 1) $$,
    'deduct_stock 返回 BOOLEAN 格式'
);

SELECT * FROM finish();
ROLLBACK;
